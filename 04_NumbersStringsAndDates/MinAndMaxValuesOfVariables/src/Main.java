public class Main {

    public static void main(String[] args) {

        /*  Минимальные и максимальные значения переменных  */
        System.out.println("---===  Byte   ===---");
        System.out.println("Min: " + Byte.MIN_VALUE);
        System.out.println("Max: " + Byte.MAX_VALUE);

        System.out.println("---===  Short  ===---");
        System.out.println("Min: " + Short.MIN_VALUE);
        System.out.println("Max: " + Short.MAX_VALUE);

        System.out.println("---=== Integer ===---");
        System.out.println("Min: " + Integer.MIN_VALUE);
        System.out.println("Max: " + Integer.MAX_VALUE);

        System.out.println("---===  Long   ===---");
        System.out.println("Min: " + Long.MIN_VALUE);
        System.out.println("Max: " + Long.MAX_VALUE);

        System.out.println("---===  Float  ===---");
        System.out.println("Min: " + -Float.MAX_VALUE);
        System.out.println("Max: " + Float.MAX_VALUE);

        System.out.println("---=== Double  ===---");
        System.out.println("Min: " + -Double.MAX_VALUE);
        System.out.println("Max: " + Double.MAX_VALUE);

        System.out.println("---== Character ==---");
        System.out.println("Min: " + (int)Character.MAX_VALUE);
        System.out.println("Max: " + (int)Character.MIN_VALUE);

    }

}
